  export function sortBlogPosts() {
    blogPosts.sort((a, b) => {
      // Sort by date in descending order
      if (a.date !== b.date) {
        return new Date(b.date) - new Date(a.date);
      }
      // If date is equal, sort by title in ascending order
      return a.title.localeCompare(b.title);
    });
  }

